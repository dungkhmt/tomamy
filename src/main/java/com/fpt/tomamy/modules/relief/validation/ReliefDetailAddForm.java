package com.fpt.tomamy.modules.relief.validation;

public class ReliefDetailAddForm {
	String reliefSessionCode;
	String reliefOrganizationCode;
	String communeCode;
	String goodCode;
	String date;
	double quantity;
	int money;
	
	public String getReliefSessionCode() {
		return reliefSessionCode;
	}
	public void setReliefSessionCode(String reliefSessionCode) {
		this.reliefSessionCode = reliefSessionCode;
	}
	public String getReliefOrganizationCode() {
		return reliefOrganizationCode;
	}
	public void setReliefOrganizationCode(String reliefOrganizationCode) {
		this.reliefOrganizationCode = reliefOrganizationCode;
	}
	public String getCommuneCode() {
		return communeCode;
	}
	public void setCommuneCode(String communeCode) {
		this.communeCode = communeCode;
	}
	public String getGoodCode() {
		return goodCode;
	}
	public void setGoodCode(String goodCode) {
		this.goodCode = goodCode;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public double getQuantity() {
		return quantity;
	}
	public void setQuantity(double quantity) {
		this.quantity = quantity;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	
	
}
